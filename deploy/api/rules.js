function corsHeaders() {
  return {
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Methods": "GET, OPTIONS",
    "Access-Control-Allow-Headers": "Content-Type, Authorization",
  };
}

export default function handler(req, res) {
  if (req.method === "OPTIONS") {
    res.setHeader("Access-Control-Allow-Origin", "*");
    res.setHeader("Access-Control-Allow-Methods", "GET, OPTIONS");
    res.setHeader("Access-Control-Allow-Headers", "Content-Type, Authorization");
    res.setHeader("Access-Control-Max-Age", "86400");
    return res.status(200).end();
  }

  if (req.method !== "GET") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const category = req.query.category || req.query.c || "";
  const key = categoryMap[category];

  if (!key || !rules[key]) {
    return res.status(200).json({
      available_categories: Object.keys(rules).map((k) => ({
        id: k,
        title: rules[k].title,
      })),
      hint: "Use ?category=<id> to get specific rules. Supported: core, female_type, top_beauty, female_score, chat, delivery, date, attraction",
    });
  }

  const rule = rules[key];

  res.setHeader("Access-Control-Allow-Origin", "*");
  res.setHeader("Cache-Control", "s-maxage=3600, stale-while-revalidate");

  return res.status(200).json({
    category: key,
    title: rule.title,
    content: rule.content,
  });
}
